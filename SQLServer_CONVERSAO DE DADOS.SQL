-- COVERSAO E CONCATENACAO
-- https://msdn.microsoft.com/en-us/library/ms191530.aspx

SELECT NOME, NASCIMENTO
FROM ALUNO
GO
/*  (FORMATO AMERICANO)
ANDRE	1981-12-09
ANA	    1987-10-09
RUI	    1981-12-09
JOAO	1982-02-08
*/

-- CORRECAO

SELECT NOME,
	CAST(DAY(NASCIMENTO) AS VARCHAR) + '/' +
    CAST(MONTH(NASCIMENTO) AS VARCHAR) + '/' +
    CAST(YEAR(NASCIMENTO) AS VARCHAR) AS "NASCIMENTO"
FROM ALUNO
GO
/*
ANDRE	9/12/1981
ANA	    9/10/1987
RUI	    9/12/1981
JOAO	8/2/1982
*/

-- OU...

SELECT NOME, 
	CONCAT(DAY(NASCIMENTO),'-',MONTH(NASCIMENTO),'-',YEAR(NASCIMENTO))
FROM ALUNO
GO
/*
ANDRE	9-12-1981
ANA	    9-10-1987
RUI	    9-12-1981
JOAO	8-2-1982
*/
